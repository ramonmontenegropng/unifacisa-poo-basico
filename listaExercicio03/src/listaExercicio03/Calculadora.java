package listaExercicio03;

public class Calculadora {

	
	public int soma(int a, int b) {
		return a + b;
	}
	
	public double soma(double a, double b) {
		return a + b;
	}
	
	public int multiplicacao(int a, int b) {
		return a * b;
	}
	
	public double multiplicacao(double a, double b) {
		return a * b;
	}
}

